// Math operations in JavaScript

let x = 10;
let y = 3;

// Addition
console.log(x + y); // Output: 13

// Subtraction
console.log(x - y); // Output: 7

// Multiplication
console.log(x * y); // Output: 30

// Division
console.log(x / y); // Output: 3.333...

// Modulus (remainder)
console.log(x % y); // Output: 1

// Exponentiation
console.log(x ** y); // Output: 1000

// Using Math object for other operations

console.log(Math.pow(x, y));   // Power: 10^3 = 1000
console.log(Math.sqrt(16));    // Square root: 4
console.log(Math.random());    // Random number between 0 and 1
